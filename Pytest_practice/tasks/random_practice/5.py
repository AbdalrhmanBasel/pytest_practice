# Python program to convert Kilometres to Miles

def kilometre_to_miles(kilometre):
    miles = kilometre * 0.62137
    return miles


def miles_to_kilometre(miles):
    kilometre = miles / 0.62137
    return kilometre


print(kilometre_to_miles(20))
print(kilometre_to_miles(30))



